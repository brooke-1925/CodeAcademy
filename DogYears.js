//This is my age.//
const myAge = 32;
//This represents the early years.//
let earlyYears = 2;
earlyYears *= 10.5;
let laterYears = myAge - 2;
// This represents the later years//
laterYears *= 4;
// This multiplies the later years by 4.//
let myAgeInDogYears = earlyYears + laterYears;
// My age in dog years combines the early and later years.//
let myName = 'Brooke'.toLowerCase();
// This represents my name //
console.log(`My name is ${myName} . I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);
